#include <iostream>
using namespace std;

int main() {
    char ch;
    cout << "Enter a character: ";
    cin >> ch;

    if (ch >= 65 && ch <= 90)
        cout << ch << " is Uppercase." << endl;
    else if (ch >= 97 && ch <= 122)
        cout << ch << " is Lowercase." << endl;
    else if (ch >= 48 && ch <= 57)
        cout << ch << " is a Digit." << endl;
    else
        cout << ch << " is a Special Character." << endl;

    return 0;
}
