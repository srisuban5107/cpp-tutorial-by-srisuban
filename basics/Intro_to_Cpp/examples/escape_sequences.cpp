#include <iostream>
using namespace std;

int main()
{
    cout << "1. New line\\n example\n";
    cout << "This is on next line\n\n";

    cout << "2. Horizontal tab\\t example\tTabbed text\n";

    cout << "3. Vertical tab\\v example\vVertical\n";

    cout << "4. Backspace\\b example: ABC\bD\n";

    cout << "5. Carriage return\\r example\rHello\n";

    cout << "6. Form feed\\f example\fEnd\n";

    cout << "7. Alert\\a example\a\n";

    cout << "8. Backslash\\\\ example: C:\\\\Cplusplus\n";

    cout << "9. Single quote\\' example: \'A\'\n";

    cout << "10. Double quote\\\" example: \"C++\"\n";

    cout << "11. Question mark\\? example: What\\?\n";

    cout << "12. Null character\\0 example: ABC\0DEF\n";

    cout << "13. Octal value \\101 = \101\n";

    cout << "14. Hexadecimal value \\x41 = \x41\n";

    return 0;
}
