#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double num = 9.0;

    cout << "Square root of " << num << " = " << sqrt(num) << endl;
    cout << "Power: " << pow(2, 3) << endl; // 2^3
    cout << "Absolute: " << abs(-15) << endl;
    cout << "Ceil of 4.3 = " << ceil(4.3) << endl;
    cout << "Floor of 4.7 = " << floor(4.7) << endl;

    return 0;
}
